package com.Java;

public class J_1DArray
{
    public static void main(String[] args)
    {
        int num[] = new int[5];

        num[0] = 3;
        num[1] = 87;
        num[2] = 65;
        num[3] = 32;
        num[4] = 76;

        for (int i = 0; i <=4; i++)
        {
            System.out.println(num[i]);
        }
    }
}
