package com.Java;

public class A_Variables
{
    public static void main(String arg [])
    {
        int a = 5;
        System.out.println(a);

        double b = 7.8;
        float c = 4.5f;
        short d = 6;
        long l = 50000000000l;
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(l);
    }
}
